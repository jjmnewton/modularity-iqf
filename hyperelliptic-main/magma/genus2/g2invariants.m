intrinsic IgusaInvariants(C::CrvHyp : Quick := false, extend := false, normalize := false) -> SeqEnum, SeqEnum
    {Compute the Igusa J-invariants of a genus 2 curve over a field.
    If Quick, the base field must not be of characteristic 2, 3, or 5.}

    require Genus(C) eq 2: "Curve must be of genus 2.";
    K := BaseField(C);
    R := PolynomialRing(K); x := R.1;
    f, h := HyperellipticPolynomials(C);

    require (Degree(h) le 3) and (Degree(f) le 6):
        "The curve must be of form y^2 + h*y = f, where h has degree at most 3 and f has degree at most 6.";

    if Quick then
        require IsUnit(K!30):
            "The integers 2, 3 and 5 must be units of the coefficient ring.";
        Js := QuickJInvariantsCurve(C : extend := extend);
        if normalize eq false then return Js; end if;
	return WPSNormalize([2,4,6,8,10,15][1..#Js], Js), [2,4,6,8,10,15][1..#Js];
    end if;

    return IgusaInvariants(f,h : extend := extend, normalize:=normalize);

end intrinsic;

intrinsic IgusaInvariantsEqual(V1::SeqEnum, V2::SeqEnum) -> BoolElt
    {Check whether Igusa Invariants V1 and V2 of two genus 2 hyperelliptic curves or of
    two binary forms of degree 8 are equivalent.}

    require (#V1 eq 6 and #V2 eq 6) or (#V1 eq 5 and #V2 eq 5)
        : "V1, V2 must be of size 5 or 6";

    if #V1 eq 6 then
        return WPSEqual([2,4,6,8,10,15], V1, V2);
    end if;

    return WPSEqual([1,2,3,4,5], V1, V2);

end intrinsic;

intrinsic DiscriminantFromIgusaInvariants(JI::SeqEnum) -> .
    {Compute the discriminant of a genus 2 hyperelliptic curve from the given
    Igusa invariants.}

    K := Universe(JI);

    require IsUnit(K!(2)) :
       "Argument must be a polynomial over a ring in which 2 is a unit.";

   require #JI eq 6 or #JI eq 5
        : "JI must be of size 5 or 6";

    return JI[5];

end intrinsic;

